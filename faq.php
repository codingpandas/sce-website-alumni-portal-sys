<h3><b>Welcome to the San Jose State University Computer Engineering Program!</b></h3>

All of this information was put together for students by students to help make your transition to San Jose State University a fun and memorable one!</br>
</br>
This guide is a helpful summary of the great benefits and time had here at the Computer Engineering program and will hopefully answer any questions you may be wondering before coming to San Jose State University.</br>
</br>
<b>What is Computer Engineering?</b></br>
Computer Engineering deals with how software and hardware work together. You learn a bit about Computer Science, Electrical Engineering and how it all ties together to make a modern day computer! Computers are more than just your desktop or laptop. Almost everything these days has a computer from your wrist watch to your car which means there are lots of job opportunities!</br>
</br>
<b>What will my first 2 years be like?</b></br>
Like any college program, most of your time will be spent in general education classes getting easy A.s and meeting students from other majors. You will take a few engineering courses but the bulk of your Computer Engineering courses come a bit later.</br>
</br>
<b>What will my last 2 years be like?</b></br>
You will be taking almost all classes relating to Computer Engineering. Most of your time will be spent in the Computer Engineering department, studying, working on projects, hanging out with friends or just relaxing in the Society for Computer Engineering club room.</br>
</br>
<b>What kinds of careers are available to me when I graduate?</b>
<ul style="margin-top: 0px;">
	<li>Here are just a few of the careers available when you graduate (<i>from computer.org</i>):</br>
	<li><b>Artificial Intelligence</b> -- Develop computers that simulate human learning and reasoning ability</br>
	<li><b>Computer Design and Engineering</b> -- Design new computer circuits, microchips, and other electronic components</br>
	<li><b>Computer Architecture</b> -- Design new computer instruction sets, and combine electronic or optical components to provide powerful but cost-effective computing</br>
	<li><b>Information Technology</b> -- Develop and manage information systems that support a business or organization</br>
	<li><b>Computer Theory</b> -- Investigate the fundamental theories of how computers solve problems, and apply the results to other areas of computer science</br>
	<li><b>Operating Systems and Networks</b> -- Develop the basic software computers use to supervise themselves or to communicate with other computers</br>
	<li><b>Software Applications</b> -- Apply computing and technology to solving problems outside the computer field - in education or medicine, for example</br>
</ul>
<b>Enough with the school talk. I want to have some FUN!</b></br>
Ok ok, so the unofficial fun side of college is the friends, parties and great memories you will have here at San Jose State University. Lucky for you the university is in the heart of downtown San Jose. There are restaurants, movie theatres, bars and a very fun night life. The school also has very well known sororities and fraternities as well as almost any type club you can think of.</br>
</br>
