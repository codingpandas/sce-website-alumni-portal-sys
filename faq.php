</ul>
</br>
<b>Benefits of the Computer Engineering program at SJSU:</b></br>
What makes the SJSU Computer Engineering program so much better than other universities? Simply put - it is the faculty and our extensive hands-on experience. You will build computers from scratch using microchips, design processors and write programs that will do amazing things. After completing your degree, you should have AT LEAST three projects to put on your resume and brag about to employers. Other universities typically don.t have students build anything unless it is for your senior design project.</br>
</br>
